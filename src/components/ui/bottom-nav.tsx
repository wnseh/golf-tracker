export function BottomNav() {
  const pathname = usePathname();

  function isActive(href: string) {
    if (href === '/') return pathname === '/';
    if (href === '/round') return pathname.startsWith('/round');
    return pathname.startsWith(href);
  }

  return (
    <nav className="fixed bottom-0 left-0 right-0 z-50 border-t border-border bg-bg/80 backdrop-blur-lg pb-[env(safe-area-inset-bottom)]">
      <div className="mx-auto max-w-lg flex justify-around items-center h-14">
        {tabs.map((tab) => {
          const active = isActive(tab.href);
          // Input tab is not directly linkable (needs round id)
          const isInput = tab.href === '/round';
          const Comp = isInput ? 'div' : Link;
          const props = isInput ? {} : { href: tab.href };

          return (
            <Comp
              key={tab.label}
              {...(props as { href: string })}
              className={`flex flex-col items-center gap-0.5 text-[10px] font-medium transition ${
                active ? 'text-accent' : 'text-text3'
              } ${isInput ? 'cursor-default' : ''}`}
            >
              {tab.icon}
              <span>{tab.label}</span>
            </Comp>
          );
        })}
      </div>
    </nav>
  );
}
